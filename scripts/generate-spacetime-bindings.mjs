import { spawnSync } from "node:child_process";
import { writeFileSync } from "node:fs";

const command = "spacetime generate --lang typescript --module-path spacetimedb --out-dir src/spacetime/module_bindings";
const outDir = "src/spacetime/module_bindings";
const provenancePath = `${outDir}/provenance.json`;
const args = [
  "generate",
  "--lang",
  "typescript",
  "--module-path",
  "spacetimedb",
  "--out-dir",
  outDir
];

const result = spawnSync("spacetime", args, {
  stdio: "inherit"
});

if (result.error) {
  if (result.error.code === "ENOENT") {
    console.error("Missing `spacetime` CLI. Install it before running binding generation.");
    console.error(`Expected command: ${command}`);
  } else {
    console.error(result.error.message);
  }
  process.exit(1);
}

if ((result.status ?? 1) !== 0) {
  process.exit(result.status ?? 1);
}

const normalize = spawnSync("node", ["scripts/normalize-spacetime-bindings.mjs"], {
  stdio: "inherit"
});

if ((normalize.status ?? 1) !== 0) {
  process.exit(normalize.status ?? 1);
}

const version = spawnSync("spacetime", ["--version"], {
  encoding: "utf8"
});

writeFileSync(
  provenancePath,
  `${JSON.stringify(
    {
      status: "official",
      generator: "spacetime generate",
      command,
      modulePath: "spacetimedb",
      outDir,
      generatedAt: new Date().toISOString(),
      cliVersion: version.status === 0 ? version.stdout.trim() : null,
      notes: "Official bindings generated by the spacetime CLI."
    },
    null,
    2
  )}\n`
);

console.log(`Wrote binding provenance: ${provenancePath}`);
process.exit(0);
